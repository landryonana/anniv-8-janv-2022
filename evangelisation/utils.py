def month_name(number):
    if number == 1:
        return "Janvier"
    elif number == 2:
        return "Février"
    elif number == 3:
        return "Mars"
    elif number == 4:
        return "Avril"
    elif number == 5:
        return "Mai"
    elif number == 6:
        return "Juin"
    elif number == 7:
        return "Juillet"
    elif number == 8:
        return "Août"
    elif number == 9:
        return "Septembre"
    elif number == 10:
        return "Octobre"
    elif number == 11:
        return "Novembre"
    elif number == 12:
        return "Décembre"
